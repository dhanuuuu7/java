public class Arithmetic {
    public static void main(String [] args){
        int p = 20 , q=5;

        System.out.println(p+q);
        System.out.println(p-q);
        System.out.println(p*q);
        System.out.println(p/q);
        System.out.println(p%q);
        System.out.println(p++);//first print then increment
        System.out.println(p--);
        System.out.println(++q);//first increment then print.
    }
}
